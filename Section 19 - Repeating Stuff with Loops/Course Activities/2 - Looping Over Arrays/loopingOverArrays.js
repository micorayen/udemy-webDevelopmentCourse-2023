// Activity: Reverse Class A order
const classA = ['John', 'Marco', 'Mico', 'Rezy'];

for (let i = 0; i < classA.length; i++) {
    console.log(i, classA[i]);
}

// Activity: Reverse Class B order
const classB = ['John', 'Marco', 'Mico', 'Rezy'];

for (let i = classB.length - 1; i >= 0; i--) {
    console.log(i, classB[i]);
}


